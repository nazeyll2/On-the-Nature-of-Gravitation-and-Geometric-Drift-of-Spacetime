# e^A_mu, rows = flat index A, columns = coordinate index mu
# This tetrad contains the radial drift b(t,r) in e^1_t.
e = Matrix([
    [c,   0,   0,             0],
    [r*b, a,   0,             0],
    [0,   0,   a*r,           0],
    [0,   0,   0,   a*r*sin(th)],
])

# e_A^mu, represented as matrix rows = coordinate index mu, columns = flat index A
e_inv = simplify(e.inv())

g = simplify(e.T * eta * e)
g_inv = simplify(g.inv())
e_det = simplify(e.det())

