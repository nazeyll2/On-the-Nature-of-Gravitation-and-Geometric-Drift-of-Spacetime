t, r, th, ph = symbols("t r theta phi", real=True)
coords = [t, r, th, ph]
coord_names = ["t", "r", "theta", "phi"]
flat_names = ["0", "1", "2", "3"]

a = Function("a")(t)
c = Function("c")(t)
b = Function("b")(t, r)

beta, kappa = symbols("beta kappa", real=True)

# Physical matter variables
rho = Function("rho")(t, r)
