def print_matrix_nonzero(title, matrix, upper=False):
    print_header(title)
    for i in range(4):
        for j in range(4):
            value = simp(matrix[i, j])
            if value != 0:
                symbol = "^" if upper else "_"
                print(f"g{symbol}{{{coord_names[i]}{coord_names[j]}}} = {value}")

